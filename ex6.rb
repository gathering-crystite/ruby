# variable to input a number of people
types_of_people = 10

# inputting a string into a variable, x
x = "There are #{types_of_people} types of people."

# inputting a string into variable named binary
binary = "binary"

# inputting a string into a variable named: do_not
do_not = "don't"

# inputting two strings into a variable
y = "Those who know #{binary} and those who #{do_not}."

# Print the out put of x (strings included)
puts x

# Print the out put of y (strings included)
puts y

# print x variable into another string.
puts "I said: #{x}."

# print y variable into another string.
puts "I also said: '#{y}'."

# input for variable named hilarious
hilarious = false

# input for joke_evaluation includes another variable
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# prints the value of the joke_evaluation variable
puts joke_evaluation

# input for w variable
w = "This is the left side of..."

# input for e variable
e = "a string with a right side."

# print strings included in w and e variables
